pub struct Palette {
    pub id: &'static str,
    pub name: &'static str,
    pub dark: bool,
    pub fg: [u8; 3],
    pub bg: [u8; 3],
    pub ansi: [[u8; 3]; 16],
}

pub struct Theme {
    pub bg: [u8; 3],
    pub fg: [u8; 3],
    pub dim_fg: [u8; 3],
    pub accent: [u8; 3],
    pub error: [u8; 3],
    pub success: [u8; 3],
    pub warning: [u8; 3],
    pub panel_bg: [u8; 3],
}

impl Palette {
    pub fn theme(&self) -> Theme {
        let accent = if self.dark { self.ansi[12] } else { self.ansi[4] };
        let panel_bg = if self.dark {
            [
                self.bg[0].saturating_add(15),
                self.bg[1].saturating_add(15),
                self.bg[2].saturating_add(15),
            ]
        } else {
            [
                self.bg[0].saturating_sub(15),
                self.bg[1].saturating_sub(15),
                self.bg[2].saturating_sub(15),
            ]
        };
        Theme {
            bg: self.bg,
            fg: self.fg,
            dim_fg: [
                (self.fg[0] as u16 * 6 / 10) as u8,
                (self.fg[1] as u16 * 6 / 10) as u8,
                (self.fg[2] as u16 * 6 / 10) as u8,
            ],
            accent,
            error: self.ansi[1],
            success: self.ansi[2],
            warning: self.ansi[3],
            panel_bg,
        }
    }
}

pub fn resolve_color(
    palette: &Palette,
    color_type: u8,
    r: u8,
    g: u8,
    b: u8,
    is_fg: bool,
    dim: bool,
) -> [u8; 3] {
    match color_type {
        0 => {
            let base = if is_fg { palette.fg } else { palette.bg };
            if dim && is_fg {
                [
                    (base[0] as u16 * 6 / 10) as u8,
                    (base[1] as u16 * 6 / 10) as u8,
                    (base[2] as u16 * 6 / 10) as u8,
                ]
            } else {
                base
            }
        }
        1 => {
            let idx = r as usize;
            let color = if idx < 16 {
                palette.ansi[idx]
            } else if idx < 232 {
                let i = idx - 16;
                [
                    ((i / 36) * 51) as u8,
                    (((i % 36) / 6) * 51) as u8,
                    ((i % 6) * 51) as u8,
                ]
            } else {
                let v = (8 + (idx - 232) * 10) as u8;
                [v, v, v]
            };
            if dim {
                [color[0] / 2, color[1] / 2, color[2] / 2]
            } else {
                color
            }
        }
        2 => {
            if dim {
                [r / 2, g / 2, b / 2]
            } else {
                [r, g, b]
            }
        }
        _ => palette.fg,
    }
}

pub fn find_palette(id: &str) -> &'static Palette {
    PALETTES.iter().find(|p| p.id == id).unwrap_or(&PALETTES[0])
}

pub fn dark_palettes() -> impl Iterator<Item = &'static Palette> {
    PALETTES.iter().filter(|p| p.dark)
}

pub fn light_palettes() -> impl Iterator<Item = &'static Palette> {
    PALETTES.iter().filter(|p| !p.dark)
}

